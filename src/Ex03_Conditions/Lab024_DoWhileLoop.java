package Ex03_Conditions;

public class Lab024_DoWhileLoop {
    public static void main(String[] args)
    {
        //Printing 1 to 9
       int  i=0;
        do {
            System.out.println(i);
            i++;
        }while (i<=10);

        //print 10 to 1

        int  j=10;
        do {
            System.out.println(j);
            j--;
        }while (j>0);
    }
}
