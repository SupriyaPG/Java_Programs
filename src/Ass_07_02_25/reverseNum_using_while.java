package Ass_07_02_25;

public class reverseNum_using_while {
    public static void main(String[] args)
    {
        //Assignment 6: Printing no 10 to 1(Reverse order)
        System.out.println("Printing reverse order no from 10 to 1 :");
        int i=10;
        while (i>=1)
        {
            System.out.println(i);
            i--;
        }
    }
}
