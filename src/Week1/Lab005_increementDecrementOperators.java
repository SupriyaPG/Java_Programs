package Week1;

public class Lab005_increementDecrementOperators {
    public static void main(String [] args)
    {
        System.out.println("----------------increment & decrement Operatos--------------");
        int a=2;

        System.out.println("a=" + a);
        a++;
        System.out.println("a++  " + a);

        int b=4;
        System.out.println("b=" + b);
        b--;
        System.out.println("b--  " + b);
    }
}
