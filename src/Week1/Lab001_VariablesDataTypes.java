package Week1;
/*
Data Type – The kind of data that it can hold. For example, int, string, float, char, etc.
Variable Name – To identify the variable uniquely within the scope.
Value – The data assigned to the variable. */

public class Lab001_VariablesDataTypes {
    public static void main(String[] args)
    {
        int i=10; //int represents integer datatype
        double d=5.5; //double represents floating numbers
        char c='S'; // char represents character datatype, it always writes in ''(single quote)
        boolean b=true; //boolean shows boolean value
        String s="Welcome to java world."; // String represents sentence which want to print, it always covered with ""(double quote)
                                        //S of String always write in Capital
        //lets print it
        System.out.println(i);
        System.out.println(d);
        System.out.println(c);
        System.out.println(b);
        System.out.println(s);
    }
}
