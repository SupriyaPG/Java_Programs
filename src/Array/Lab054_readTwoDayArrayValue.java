package Array;

public class Lab054_readTwoDayArrayValue {
    public static void main(String[] args) {
        int a[][]={{101,200},
                {103,104},
                {105,106}
        };

        System.out.print(a[0][0]+" "); //first row, first column
        System.out.println(a[0][1]); //first row second column

        System.out.print(a[1][0]+" ");
        System.out.println(a[1][1]);

        System.out.print(a[2][0]+" ");
        System.out.println(a[2][1]);
    }
}
